--------------------------------------------------------
--  DDL for Function RECUPERER_SCORE_JEU
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE FUNCTION "PIPELINE"."RECUPERER_SCORE_JEU" (param_id_jeu NUMBER) RETURN score_jeu IS
    CURSOR cursor_jeu_set (VAR_ID_SET NUMBER) IS 
        SELECT ID_JEU FROM JEU_MATCH WHERE ID_SET = VAR_ID_SET;
    CURSOR cursor_jeu (VAR_ID_JEU NUMBER) IS 
        SELECT ID_POINT_ENUMERATION, ID_EQUIPE FROM POINT_MATCH WHERE ID_JEU = VAR_ID_JEU;
    ligne_cursor NUMBER;
    ligne_point cursor_jeu%rowtype;
    var_id_set NUMBER;
    var_id_equipe_1 NUMBER;
    var_id_equipe_2 NUMBER;
    cpt_equipe_1 NUMBER := 0;
    cpt_equipe_2 NUMBER := 0;
    BEGIN  
        SELECT ID_SET INTO var_id_set FROM JEU_MATCH WHERE ID_JEU = param_id_jeu;

        SELECT ID_EQUIPE INTO var_id_equipe_1 FROM POINT_MATCH WHERE ID_JEU = param_id_jeu AND ROWNUM = 1 GROUP BY ID_EQUIPE ORDER BY ID_EQUIPE;
        SELECT ID_EQUIPE INTO var_id_equipe_2 FROM POINT_MATCH WHERE ID_JEU = param_id_jeu AND ROWNUM = 1 GROUP BY ID_EQUIPE ORDER BY ID_EQUIPE DESC;

        OPEN cursor_jeu_set(var_id_set);
        LOOP
            FETCH cursor_jeu_set INTO ligne_cursor;

            OPEN cursor_jeu(ligne_cursor);
            LOOP
                FETCH cursor_jeu INTO ligne_point;
                IF ligne_point.ID_POINT_ENUMERATION = 6 THEN
                    IF ligne_point.ID_EQUIPE = var_id_equipe_1 THEN
                        cpt_equipe_1 := cpt_equipe_1 + 1;
                    ELSE
                        cpt_equipe_2 := cpt_equipe_2 + 1;
                    END IF;
                END IF;
            END LOOP;
            CLOSE cursor_jeu;
        END LOOP;
        CLOSE cursor_jeu_set;
    RETURN score_jeu(cpt_equipe_1, cpt_equipe_2);
    END;

/
