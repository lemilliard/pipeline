            IF id_point_equipe_1 = nb - 2 THEN
                insert_nouveau_jeu(:new.id_equipe, id_equipe_2, id_set);
                :new.ID_POINT_ENUMERATION := MOD((id_point_equipe_1 + 1), nb);
